        /// <summary>
        /// kN*m -> N*m
        /// 1 kilonewton meter [kN*m] =  1000 newton meter [N*m]
        /// </summary>
        public static double KilonewtonMeter_2_NewtonMeter(double inVal)                => inVal * 1000;
        /// <summary>
        /// N*m -> kN*m
        /// vv: 1000 newton meter [N*m] to 1 kilonewton meter [kN*m]
        /// </summary>
        public static double NewtonMeter_2_KilonewtonMeter(double inVal)                => inVal / 1000;

        /// <summary>
        /// mN*m -> N*m
