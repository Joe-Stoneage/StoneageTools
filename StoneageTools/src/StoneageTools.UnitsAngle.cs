
// Reference: https://www.unitconverters.net/

namespace StoneageTools.Units
{
    public static partial class StoneageToolsUnits
    {
        #region angle, base: degree

        /// <summary>
        /// rad -> °
        /// 1 radian [rad] to vv: 57.2957795131 degree [°]
        /// </summary>
        public static double Radian_2_Degree(double inVal)                              => inVal * 57.2957795131;
        /// <summary>
        /// ° -> rad
        /// vv: 57.2957795131 degree [°] to 1 radian [rad]
        /// </summary>
        public static double Degree_2_Radian(double inVal)                              => inVal / 57.2957795131;

        /// <summary>
        /// ^g -> °
        /// 1 grad [^g] to vv: 0.9 degree [°]
        /// </summary>
        public static double Grad_2_Degree(double inVal)                                => inVal * 0.9;
        /// <summary>
        /// ° -> ^g
        /// vv: 0.9 degree [°] to 1 grad [^g]
        /// </summary>
        public static double Degree_2_Grad(double inVal)                                => inVal / 0.9;

        /// <summary>
        /// ' -> °
        /// 1 minute ['] to vv: 0.0166666667 degree [°]
        /// </summary>
        public static double Minute_2_Degree(double inVal)                              => inVal * 0.0166666667;
        /// <summary>
        /// ° -> '
        /// vv: 0.0166666667 degree [°] to 1 minute [']
        /// </summary>
        public static double Degree_2_Minute(double inVal)                              => inVal / 0.0166666667;

        /// <summary>
        /// " -> °
        /// 1 second ["] to vv: 0.0002777778 degree [°]
        /// </summary>
        public static double Second_2_Degree(double inVal)                              => inVal * 0.0002777778;
        /// <summary>
        /// ° -> "
        /// vv: 0.0002777778 degree [°] to 1 second ["]
        /// </summary>
        public static double Degree_2_Second(double inVal)                              => inVal / 0.0002777778;

        /// <summary>
        /// Gon -> °
        /// 1 gon to vv: 0.9 degree [°]
        /// </summary>
        public static double Gon_2_Degree(double inVal)                                 => inVal * 0.9;
        /// <summary>
        /// ° -> Gon
        /// vv: 0.9 degree [°] to 1 gon
        /// </summary>
        public static double Degree_2_Gon(double inVal)                                 => inVal / 0.9;

        /// <summary>
        /// Sign -> °
        /// 1 sign to vv: 30 degree [°]
        /// </summary>
        public static double Sign_2_Degree(double inVal)                                => inVal * 30;
        /// <summary>
        /// ° -> Sign
        /// vv: 30 degree [°] to 1 sign
        /// </summary>
        public static double Degree_2_Sign(double inVal)                                => inVal / 30;

        /// <summary>
        /// Mil -> °
        /// 1 mil to vv: 0.05625 degree [°]
        /// </summary>
        public static double Mil_2_Degree(double inVal)                                 => inVal * 0.05625;
        /// <summary>
        /// ° -> Mil
        /// vv: 0.05625 degree [°] to 1 mil
        /// </summary>
        public static double Degree_2_Mil(double inVal)                                 => inVal / 0.05625;

        /// <summary>
        /// r -> °
        /// 1 revolution [r] to vv: 360 degree [°]
        /// </summary>
        public static double Revolution_2_Degree(double inVal)                          => inVal * 360;
        /// <summary>
        /// ° -> r
        /// vv: 360 degree [°] to 1 revolution [r]
        /// </summary>
        public static double Degree_2_Revolution(double inVal)                          => inVal / 360;

        /// <summary>
        /// Circle -> °
        /// 1 circle to vv: 360 degree [°]
        /// </summary>
        public static double Circle_2_Degree(double inVal)                              => inVal * 360;
        /// <summary>
        /// ° -> Circle
        /// vv: 360 degree [°] to 1 circle
        /// </summary>
        public static double Degree_2_Circle(double inVal)                              => inVal / 360;

        /// <summary>
        /// Turn -> °
        /// 1 turn to vv: 360 degree [°]
        /// </summary>
        public static double Turn_2_Degree(double inVal)                                => inVal * 360;
        /// <summary>
        /// ° -> Turn
        /// vv: 360 degree [°] to 1 turn
        /// </summary>
        public static double Degree_2_Turn(double inVal)                                => inVal / 360;

        /// <summary>
        /// Quadrant -> °
        /// 1 quadrant to vv: 90 degree [°]
        /// </summary>
        public static double Quadrant_2_Degree(double inVal)                            => inVal * 90;
        /// <summary>
        /// ° -> Quadrant
        /// vv: 90 degree [°] to 1 quadrant
        /// </summary>
        public static double Degree_2_Quadrant(double inVal)                            => inVal / 90;

        /// <summary>
        /// RightAngle -> °
        /// 1 right angle to vv: 90 degree [°]
        /// </summary>
        public static double RightAngle_2_Degree(double inVal)                          => inVal * 90;
        /// <summary>
        /// ° -> RightAngle
        /// vv: 90 degree [°] to 1 right angle
        /// </summary>
        public static double Degree_2_RightAngle(double inVal)                          => inVal / 90;

        /// <summary>
        /// Sextant -> °
        /// 1 sextant to vv: 60 degree [°]
        /// </summary>
        public static double Sextant_2_Degree(double inVal)                             => inVal * 60;
        /// <summary>
        /// ° -> Sextant
        /// vv: 60 degree [°] to 1 sextant
        /// </summary>
        public static double Degree_2_Sextant(double inVal)                             => inVal / 60;

        #endregion
    }
}
