        /// <summary>
        /// N*m -> mN*m
        /// vv: 0.001 newton meter [N*m] to 1 millinewton meter [mN*m]
        /// </summary>
        public static double NewtonMeter_2_MillinewtonMeter(double inVal)               => inVal / 0.001;

